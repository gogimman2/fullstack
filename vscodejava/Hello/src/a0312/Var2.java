package a0312;

public class Var2 {
    public static void main(String[] args) {
        int a;  // 변수선언
        a = 20;  // 10 -> 20 변경
        // a를 선언한 변수에 처음으로 값을 대입하는 것을 변수의 초기화
        System.out.println(a);
        System.out.println(a);
        System.out.println(a);
        // 변수 -> 변할 수 있는 수 a 값은 언제든 변경 가능하다.
    }
}
