package a0313;

public class Operator1 {
    public static void main(String[] args) {
        // 변수 초기화
        int a = 5;
        int b = 2;

        // 덧셈
        int sum = a + b;
        System.out.println("a + b = " + sum);  // 출력
        
        int sum1 = a - b;
        System.out.println("a - b = " + sum1);

        int sum2 = a * b;
        System.out.println("a * b = " + sum2);

        int sum3 = a / b;
        System.out.println("a / b = " + sum3);

        int sum4 = a % b;
        System.out.println("a % b = " + sum4);
    }
}
